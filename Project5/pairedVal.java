
public class pairedVal {

	private Integer i;
	private Core c;

	public pairedVal(Integer i, Core c) {
		this.i = i;
		this.c = c;
	}

	public Integer getInt() {

		return this.i;
	}

	public Core getType() {

		return this.c;
	}

	public void setInt(Integer i) {

		this.i = i;

	}

	public void setType(Core c) {

		this.c = c;
	}

}
